import turtle
turtle.shape("classic")
turtle.forward(200)
#forwoard = 앞으로 200만큼 이동
turtle.right(90)
#오른쪽으로 90 방향회전
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.done()

#turtle = 거북이, circle = 원, classic = 화살표
